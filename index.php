      В оборонительный период битвы Красная армия отстояла Москву. Наступление противника, несмотря на успехи лета — осени 1941 года, было остановлено.
      Враг не сумел достичь своей главной цели блицкрига — уничтожить советские вооруженные силы до наступления зимы. Германия оказалась перед неизбежностью затяжной войны с СССР.
      С конца октября по начало декабря 1941 года удалось укрепить фронт и подтянуть из глубины страны свежие дивизии и вооружение с техникой. 
      В состав только Западного фронта были переданы три общевойсковые армии (1-я ударная, 20-я и 10-я), девять стрелковых и две кавалерийские дивизии, восемь стрелковых, 
      шесть танковых бригад и большое количество специальных частей.
      Калининский фронт и правое крыло Юго-Западного фронта также были заметно пополнены людьми и вооружением. Авиационные соединения этих фронтов были пополнены частями 
      Московского военного округа, 6-го истребительного корпуса ПВО и дальней бомбардировочной авиации из резерва Главного Командования.
      К началу декабрьского контрнаступления насчитывалось уже более 1,1 млн человек, 7652 орудий и миномётов, 415 установок реактивной артиллерии, 774 танка (в том числе 222 тяжёлых и средних)
      и 1 тыс. самолётов. В немецкой группе армий «Центр» было 1 708 тыс. человек, около 13 500 орудий и миномётов, 1170 танков и 615 самолётов.
      С целью предотвращения дальнейшего усиления группы армий «Центр» за счёт войск групп армий «Север» и «Юг», советскими войсками были проведены наступательные операции: 
      Тихвинская (с 10 ноября) и Ростовская (с 17 ноября).
