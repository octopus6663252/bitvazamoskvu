      Война с Советским Союзом, несмотря на первоначальные победы, развивалась для немецкого командования несколько по иному сценарию нежели с Польшей или западноевропейскими странами.
      План блицкрига (Операция «Барбаросса») предполагал взятие Москвы в течение первых 10-12 недель войны. Однако, несмотря на успехи вермахта в первые дни наступления, 
      усилившееся сопротивление Красной армии и ряд объективных причин помешали его выполнению.

      Только Смоленское сражение июля — сентября 1941 года задержало продвижение захватчиков к Москве на два месяца. Германские стратеги не смогли в полной мере предусмотреть всех издержек, 
      связанных со значительным расширением фронта, износом материальной части ударных группировок и падением наступательного духа солдат и офицеров в случае непредвиденного упорного 
      сопротивления противника.

      К началу сентября 1941 года московское направление оставалось главным для верховного немецкого командования. В телеграмме ОКХ от 31 июля 1941 года группе армий «Центр» 
      было приказано по-прежнему «готовиться к наступлению на Москву».
