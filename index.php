      В ходе сражения немецкие войска потерпели ощутимое поражение. В результате контрнаступления и общего наступления они были отброшены на 100—250 км. Полностью были освобождены Тульская, 
      Рязанская и Московская области, многие районы Калининской, Смоленской и Орловской областей.
      В то же время силы вермахта смогли сохранить фронт и Ржевско-Вяземский плацдарм. Советским войскам не удалось разгромить группу армий «Центр». 
      Таким образом, решение вопроса об обладании стратегической инициативой было отложено до летней кампании 1942 года.
      Вопреки распространённой сегодня точке зрения, многие немецкие генералы высоко оценивали боевые качества красноармейцев. Через месяц боёв Гальдер записывает окончательный 
      и крайне неприятный для германского командования вывод, сделанный фельдмаршалом Браухичем: «Своеобразие страны и своеобразие характера русских придаёт кампании особую специфику. 
      Первый серьёзный противник».
      К тому же выводу приходит и командование группы армий «Юг»: «Силы, которые нам противостоят, являются по большей части решительной массой, 
      которая в упорстве ведения войны представляет собой нечто совершенно новое по сравнению с нашими бывшими противниками. Мы вынуждены признать, 
      что Красная Армия является очень серьёзным противником… Русская пехота проявила неслыханное упорство прежде всего в обороне стационарных укреплённых сооружений. 
      Даже в случае падения всех соседних сооружений некоторые ДОТы, призываемые сдаться, держались до последнего человека».
